package IntroductionJava;

public class DeclareAndUseVariables {
    public static void main(String[] args) {
        int i = 10; // Kieu so nguyen co gia tri la 10;
        float f = 20.5f; // Kieu so thuc nho co gia tri la 20.5;
        double d = 20.5; // Kieu so thuc lon co gia tri la 20.5;
        boolean b = true; // Kieu logic co gia tri la true;
        char c = 'a'; // Kieu ky tu co gia tri la 'a';
        String s = "Hà Nội";// Kieu chuoi co gia tri la chuoi "Ha Noi";
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
    }
}
